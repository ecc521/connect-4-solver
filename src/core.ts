export enum Player {
  P1 = "P1", // Moves first
  P2 = "P2", // Moves second
}

export enum Outcome {
  Win = "Win",
  Loss = "Loss",
  Draw = "Draw",
}

/**
 * Thrown when a queued task is cancelled because stop() was called before it could execute.
 * Consumers should catch this and return null / discard the result — it is not an error condition.
 */
export class SolverAbortedError extends Error {
  constructor() {
    super("Solver task was cancelled by stop()");
    this.name = "SolverAbortedError";
  }
}

export interface Connect4SolverOptions {
  width?: number;
  height?: number;
  cacheSizeMb?: number;
  heuristic?: boolean;
}

export interface AnalyzeOptions {
  threads?: number;
  maxDepth?: number;
  timeoutMs?: number;
  book?: { ptr: number };
  heuristic?: boolean;
  weak?: boolean;
}

export interface Evaluation {
  eval: {
    value: number; // Normalized float (+Infinity/-Infinity for exact wins, score/100 for heuristic)
  };
  outcome?: Outcome; // Only present on exact solutions
  winner?: Player | null; // null when Draw, only present on exact solutions
  movesToEnd?: number | null; // null when Draw, only present on exact solutions
  score: number; // raw score (positive = current player winning)
}

export interface PositionAnalysis {
  position: string; // Validated position (may differ if input was invalid)
  originalPosition: string; // Raw input string
  currentPlayer: Player; // Whose turn it is at the analyzed position
  evaluation: Evaluation | null; // Overall evaluation of the position
  moveOptions: (Evaluation | null)[]; // Evaluation for playing in each column (1 to board width)
  depthReached?: number; // Actual depth reached during search (heuristic only)
  isHeuristic: boolean; // True if the analysis was generated by a heuristic engine
  bestMove?: number; // The 0-indexed column of the best move (solve() only)
  nodes?: number; // Total number of positions searched (solve() only)
  aborted?: boolean; // True if the search was aborted prematurely (exact mode only)
}

